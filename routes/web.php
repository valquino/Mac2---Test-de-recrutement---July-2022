*/

Route::group(['namespace' => 'App\Http\Controllers'], function()
{   
    /**
     * Home Routes
     */
    Route::get('/', 'HomeController@index')->name('home.index');

    /**
     * Contact Routes
     */
    Route::get('/contact', 'ContactController@show')->name('contact.show');
    Route::post('/contact', 'ContactController@handle')->name('contact.perform');

    Route::group(['middleware' => ['guest']], function() {
        /**
         * Register Routes
         */
        Route::get('/register', 'RegisterController@show')->name('register.show');
        Route::post('/register', 'RegisterController@register')->name('register.perform');

        /**
         * Login Routes
         */
        Route::get('/login', 'LoginController@show')->name('login.show');
        Route::post('/login', 'LoginController@login')->name('login.perform');
    });

    Route::group(['middleware' => ['auth']], function() {
        /**
         * Logout Routes
         */
        Route::get('/logout', 'LogoutController@perform')->name('logout.perform');

        /**
         * Messages List Routes
         */
        Route::get('/messages', 'MessagesController@messagesList')->name('messages.messagesList');
        Route::get('/messages-delete/{id}', 'MessagesController@delete')->name('message.delete');
    });
});
